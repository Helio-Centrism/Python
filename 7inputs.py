#Input in Python 
#Python provides numerous functions that enable you to read input from the user. The input from the user can be read using the input() function.
#The input() function reads a line entered on a console by an input device such as a keyboard and convert it into a string and returns it.

# Example 1: How input() function works in Python

a = input("Enter your name: ")
print("Hello", a)

# Example 2: Taking integer input in Python

b = int(input("Enter your age: "))
print("Your age is: ", b)

# Example 3: Taking float input in Python

c = float(input("Enter your cgpa: "))
print("Your cgpa is: ", c)
